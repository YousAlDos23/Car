
public class Car {

	private String name;
	private double topSpeed;
	
	//getter
	public String getName() {
		return name;
	}
	//setter
	public void setName(String newName) {
		this.name = newName;
	}
	
	public double getTopSpeed() {
		return topSpeed;
	}
	
	public void setTopSpeed(double newTopSpeed) {
		this.topSpeed = newTopSpeed;
	}
	
	public void getTopSpeedMPH(double newTopSpeed) {
		this.topSpeed = newTopSpeed;
	}

	public void getTopSpeedKMH(double newTopSpeed) {
		this.topSpeed = newTopSpeed;
}
}


